@RestControllerAdvice
public class MyExceptionHandler {
    public static final String DEFAULT_ERROR_VIEW = "unauthorized";

    @ExceptionHandler(value = AuthorizationException.class)
    public Object defaultErrorHandler(HttpServletRequest request, AuthorizationException e) throws Exception {
        if (HttpServletUtils.jsAjax(request)) {
            return ResponseResult.fail("没有权限");
        }
        return new ModelAndView(DEFAULT_ERROR_VIEW);
    }

}
